package org.csi.yucca.adminapi.util;

public enum DatasourceGroupStatus {
	
	DRAFT(1), FROZEN(2), DISMISSED(3);
	private int id;
	
	DatasourceGroupStatus(int id){
		this.setId(id);
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	
}
