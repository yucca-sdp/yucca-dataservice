package org.csi.yucca.adminapi.delegate.beans.httpresponse;

public class TenantProductContactMetadataHttpResponse {

	private String id;
	private String uri;
	private String type;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUri() {
		return uri;
	}

	public void setUri(String uri) {
		this.uri = uri;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
}
